export function getThemeById(themeId: string): ColorTheme | undefined {
  return colorThemes.find((theme) => theme.id === themeId);
}

export function applyTheme(themeId: string): void {
  const theme = getThemeById(themeId);
  if (!theme) {
    logger.warn(`Theme with id "${themeId}" not found`);
    return;
  }

  const root = document.documentElement;

  // Apply all CSS variables from the theme
  Object.entries(theme.cssVariables).forEach(([key, value]) => {
    root.style.setProperty(key, value);
  });

  // Remove old theme classes and add new one
  root.classList.remove(
    "kawaii-pink",
    "blue-scholar",
    "midnight-dark",
    "primary-pop",
    "primary-pop-dark",
    "neon-future",
    "neon-future-dark",
    "solarized-warm",
    "solarized-dark",
    "amethyst-haze",
    "amethyst-haze-dark",
    "nature",
    "nature-dark",
    "dark-matter",
    "dark-matter-dark",
    "ocean-breeze",
    "ocean-breeze-dark",
    "tangerine",
    "tangerine-dark",
    "lazy-beach",
    "lazy-beach-dark",
    "caffeine",
    "caffeine-dark",
    "soft-pop",
    "soft-pop-dark",
    "dark"
  );
  root.classList.add(themeId);

  // For backwards compatibility with dark mode detection
  if (
    themeId === "midnight-dark" ||
    themeId === "primary-pop-dark" ||
    themeId === "neon-future-dark" ||
    themeId === "solarized-dark" ||
    themeId === "amethyst-haze-dark" ||
    themeId === "nature-dark" ||
    themeId === "dark-matter-dark" ||
    themeId === "ocean-breeze-dark" ||
    themeId === "tangerine-dark" ||
    themeId === "lazy-beach-dark" ||
    themeId === "caffeine-dark" ||
    themeId === "soft-pop-dark"
  ) {
    root.classList.add("dark");
  }
}
